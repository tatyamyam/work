package com.example.tatyamyam.form;

public class BloodPressureForm {
	private int s_Bp;
	private int d_Bp;
	private int puls;
	
	public int getS_Bp() {
		return s_Bp;
	}
	
	public int getD_Bp() {
		return d_Bp;
	}
	
	public int getPuls() {
		return puls;
	}
	
	public void setS_Bp(int s_Bp) {
		this.s_Bp = s_Bp;
	}
	
	public void setD_Bp(int d_Bp) {
		this.d_Bp = d_Bp;
	}
	
	public void setPuls(int puls) {
		this.puls = puls;
	}
	

}
